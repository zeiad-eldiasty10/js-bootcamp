let input =prompt("enter the first number");
let operator =prompt("enter the operator");
let input1 =prompt("enter the second number");

if(operator==="+"){
    console.log(input+input1);
}
else if(operator==="*"){
    console.log(input*input1);
}
else if(operator==="-"){
    console.log(input-input1);
}
else if(operator==="/"){
    console.log(input/input1);
}


